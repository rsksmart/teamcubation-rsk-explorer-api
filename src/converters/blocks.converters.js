function rawBlockToEntity ({
  number,
  hash,
  parentHash,
  sha3Uncles,
  logsBloom,
  transactionsRoot,
  stateRoot,
  receiptsRoot,
  miner,
  difficulty,
  totalDifficulty,
  extraData,
  size,
  gasLimit,
  gasUsed,
  timestamp,
  transactions,
  uncles,
  minimumGasPrice,
  bitcoinMergedMiningHeader,
  bitcoinMergedMiningCoinbaseTransaction,
  bitcoinMergedMiningMerkleProof,
  hashForMergedMining,
  paidFees,
  cumulativeDifficulty,
  _received
}) {
  return {
    number,
    hash,
    parentHash,
    sha3Uncles,
    logsBloom,
    transactionsRoot,
    stateRoot,
    receiptsRoot,
    miner,
    difficulty,
    totalDifficulty,
    extraData,
    size,
    gasLimit,
    gasUsed,
    timestamp,
    transactions: JSON.stringify(transactions),
    uncles: JSON.stringify(uncles),
    minimumGasPrice,
    bitcoinMergedMiningHeader,
    bitcoinMergedMiningCoinbaseTransaction,
    bitcoinMergedMiningMerkleProof,
    hashForMergedMining,
    paidFees,
    cumulativeDifficulty,
    received: _received
  }
}

